                <h1 class="display-2">History.</h1>

                <p class="lead">The journey to establishing P.C.E.A Baraka Church began in 2007 when Baraka District was formed under P.C.E.A Mukuyu Church. For nearly a decade, efforts to establish a new church faced challenges, mainly due to difficulties in securing suitable land. However, the need for a more accessible place of worship grew as congregants, including children, struggled with the long distances to P.C.E.A Mukuyu, often facing harsh weather conditions that resulted in late arrivals or missed services.  <br>

                In 2016, a determined effort was made to finally establish the church. Through resource mobilization and support from the Outreach Foundation of the USA, a temporary church structure was constructed, and the inaugural service was held on August 21, 2016. The church was entrusted to the leadership of Elders Faith Macharia (Imani District), John Murigu (Neema District), and Lucas Mwaura (Shakainah District).<br>

                Several key individuals, including Mr. John Karonyo, Mr. Willy Ngunu, Mr. Edward Kiarie, Mr. Stephen Mwangi (Maximum), and Mr. Joseph Muiruri, played significant roles in securing local administration approval and community support for the church's establishment. Their unwavering commitment and hard work ensured the successful foundation of P.C.E.A Baraka Church, laying a strong foundation for its growth and expansion.<br>
