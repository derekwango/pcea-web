                <h1 class="display-2">Development of the Sanctuary</h1>

                <p class="lead">
                    Recognizing the need for a more permanent and modern place of worship, P.C.E.A Baraka Church has embarked on a mission to design and construct a modern sanctuary and associated structures to support the growing needs of its congregants. This development aims to enhance the worship experience, create a welcoming environment, and provide adequate facilities for various church ministries.
<br>
                    The church has set a financial target of Kshs 70 million to achieve this goal within the 2025-2029 strategic plan period. Funds will be raised through offerings, tithes, thanksgiving contributions, fundraising events, and goodwill donations from well-wishers. The successful completion of this project will not only provide a dedicated space for worship but also strengthen fellowship and community engagement within the church.
<br>
                    A structured monitoring and evaluation framework has been established to ensure effective implementation, with an appointed committee overseeing progress and alignment with the church's broader strategic objectives. Through faith, commitment, and collective effort, P.C.E.A Baraka Church looks forward to realizing this vision and furthering its mission of spreading the Gospel and serving the community.
