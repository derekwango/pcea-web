    <script type="text/javascript">
function getNextSundayAt830() {
    const now = new Date();
    const dayOfWeek = now.getDay();  // Get current day of the week (0 = Sunday, 6 = Saturday)
    const daysUntilSunday = (7 - dayOfWeek) % 7; // Calculate days until the next Sunday
    const nextSunday = new Date(now);
    
    // Set next Sunday date
    nextSunday.setDate(now.getDate() + daysUntilSunday);
    nextSunday.setHours(8, 30, 0, 0); // Set time to 8:30 AM

    if (nextSunday < now) {
        // If it's already past 8:30 AM on the current Sunday, set it to the next Sunday
        nextSunday.setDate(nextSunday.getDate() + 7);
    }

    return nextSunday.getTime();  // Return the timestamp for next Sunday at 8:30 AM
}

// Initialize the countdown
function startCountdown() {
    const countDownDate = getNextSundayAt830();  // Get the next Sunday at 8:30 AM

    const x = setInterval(function() {
        const now = new Date().getTime();
        const distance = countDownDate - now;

        const days = Math.floor(distance / (1000 * 60 * 60 * 24));
        const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
        const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
        const seconds = Math.floor((distance % (1000 * 60)) / 1000);

        // Display the result in the elements
        document.getElementById('days').innerHTML = days;
        document.getElementById('hours').innerHTML = hours;
        document.getElementById('minutes').innerHTML = minutes;
        document.getElementById('seconds').innerHTML = seconds;

        // If the countdown reaches 0, reset for the next Sunday
        if (distance < 0) {
            clearInterval(x);
            startCountdown();  // Reset the countdown for the next Sunday
        }
    }, 1000);
}

// Start the countdown when the page loads
window.onload = function() {
    startCountdown();
};

